      <p class="text-xl leading-relaxed text-base-content/80 border-l-4 border-primary pl-5 mb-8">
        Robert Gerstmann fue uno de los fotógrafos más prolíficos y visionarios que recorrió Chile
        y América Latina durante la primera mitad del siglo XX, dejando un testimonio visual
        extraordinario de una época irrepetible.
      </p>

      <!-- Sección: Vida -->
      <section class="mb-10">
        <h2 class="text-2xl font-bold mb-4 pb-2 border-b border-base-300">Vida y trayectoria</h2>
        <div class="prose prose-base max-w-none text-base-content/80 space-y-4">
          <p>
            Nacido en 1896 en Rusia de padres alemanes, Gerstmann llegó a Chile en la década de 1920 portando
            su cámara como herramienta de exploración. Con una mirada atenta y sensible,
            recorrió ciudades, puertos, desiertos y valles, construyendo un registro visual
            de Chile que hoy constituye un patrimonio histórico de primer orden.
          </p>
          <p>
            A lo largo de más de tres décadas de trabajo, capturó la vida cotidiana de las
            personas comunes, los paisajes del norte árido, la efervescencia de los puertos,
            la tranquilidad de los campos del sur y la transformación acelerada de las ciudades.
            Su lente no distinguía entre lo monumental y lo íntimo: ambos merecían ser recordados.
          </p>
          <p>
            Falleció en 1964 dejando atrás una colección de más de 47.000 fotografías,
            muchas de las cuales permanecieron inéditas durante décadas. Hoy, ese legado
            es custodiado y puesto a disposición del público a través del archivo ROGER.
          </p>
        </div>
      </section>

      <!-- Sección: Obra -->
      <section class="mb-10">
        <h2 class="text-2xl font-bold mb-4 pb-2 border-b border-base-300">Su obra fotográfica</h2>
        <div class="prose prose-base max-w-none text-base-content/80 space-y-4">
          <p>
            La colección Gerstmann abarca retratos de personas anónimas y figuras públicas,
            escenas urbanas de Santiago, Valparaíso y Antofagasta, actividades económicas
            como la minería del salitre, la pesca artesanal y la agricultura, además de
            manifestaciones culturales, fiestas populares y monumentos naturales.
          </p>
          <p>
            Lo que hace única esta colección no es solo su extensión, sino la coherencia
            de su mirada: Gerstmann fotografiaba con curiosidad antropológica y sensibilidad
            estética, logrando imágenes que son a la vez documentos históricos y obras de arte.
          </p>
        </div>
      </section>

      <!-- Sección: El Proyecto ROGER -->
      <section class="mb-10">
        <h2 class="text-2xl font-bold mb-4 pb-2 border-b border-base-300">El proyecto ROGER</h2>
        <div class="prose prose-base max-w-none text-base-content/80 space-y-4">
          <p>
            ROGER es un archivo digital creado para preservar, catalogar y dar acceso al
            público a la colección fotográfica de Robert Gerstmann. El proyecto nace de la
            convicción de que este patrimonio visual pertenece a todos y merece ser accesible,
            comprensible y vivo.
          </p>
          <p>
            A través de este archivo, cualquier persona puede explorar miles de imágenes,
            leer el contexto histórico de cada fotografía, buscar por lugar, época o tema,
            y descubrir los personajes y paisajes que Gerstmann inmortalizó. Las descripciones
            e historias que acompañan cada imagen han sido elaboradas con rigor, distinguiendo
            siempre entre lo que se sabe con certeza y lo que se puede inferir.
          </p>
          <p>
            El proyecto es una iniciativa de investigación aplicada desarrollada en colaboración
            con la <strong>Universidad Católica del Norte</strong>, con el apoyo del programa
            FONDEF de ANID.
          </p>
        </div>
      </section>

      <!-- Sección: Misión -->
      <section class="mb-10">
