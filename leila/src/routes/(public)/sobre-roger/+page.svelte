          <h2 class="text-xl font-bold mb-3">Nuestra misión</h2>
          <p class="text-base-content/70 leading-relaxed">
            Hacer que la memoria fotográfica de Chile sea accesible, comprensible y significativa
            para todas las personas, independientemente de su formación o lugar de origen.
            Creemos que las imágenes históricas son puentes hacia el pasado que nos ayudan a
            entender quiénes somos hoy.
          </p>
        </div>
